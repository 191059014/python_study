"""
变量定义、数据类型
"""
bool_val = True  # 布尔型
int_var = 10  # 整形
double_var = 9.5  # 浮点型
str_var = "zhangsan"  # 字符串
list_var = [1, 2, 3, 4, 5]  # 列表
tuple_var1 = ("zhangsan", "lisi", "wangwu")  # 元祖
tuple_var2 = 1, 2, 3  # 元祖
dict_var = {"name": "zhangsan", "age": 20}  # 字典
set_var1 = {"zhangsan", "lisi"}  # set集合

print(bool_val)
print(int_var)
print(double_var)
print(str_var)
print(list_var)
print(tuple_var1)
print(tuple_var2)
print(dict_var)
print(set_var1)
